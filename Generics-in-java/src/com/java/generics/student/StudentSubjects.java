package com.java.generics.student;

public class StudentSubjects {
	String math;
	String english;
	String economics;
	String artsAndScience;
	String computers;

	public StudentSubjects() {}

	public String getMath() {
		return math;
	}

	public void setMath(String math) {
		this.math = math;
	}

	public String getEnglish() {
		return english;
	}

	public void setEnglish(String english) {
		this.english = english;
	}

	public String getEconomics() {
		return economics;
	}

	public void setEconomics(String economics) {
		this.economics = economics;
	}

	public String getArtsAndScience() {
		return artsAndScience;
	}

	public void setArtsAndScience(String artsAndScience) {
		this.artsAndScience = artsAndScience;
	}

	public String getComputers() {
		return computers;
	}

	public void setComputers(String computers) {
		this.computers = computers;
	}
	
	
}
